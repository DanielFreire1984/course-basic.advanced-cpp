//Function to calculate the square area
float areaQuad(float side)
{
	return side * side;
}

//Function to calculate the triangle rectangle area
float areaTrianguleEqui(float base, float height)
{
	return (base * height) /2;
}

//Function to calculate rectangle area
float areaRectangule(float base, float height)
{
	return base * height;
}
